     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $url = $request->segment(2).'/'.$request->segment(3);
        $business = ModelsBusiness::where('url', '=', $url)->first();
        return view( $business->view, [
            'business' => $business,
            'question_title' => $business->question_title,
        ]);
    }

    public function intro(Request $request)
    {
        $uri = $request->segment(2);
        $models = ModelsBusiness::where('type', '=', $uri)->get();
        return view("business.{$uri}.main",[
            'models' => $models,
            'page' => $uri,
        ]);
    }

